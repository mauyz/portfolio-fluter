class Experience {
  final String job;
  final String date;
  final String enterprise;
  final String type;
  final String location;
  final List<String> task;
  final List<String> stack;

  Experience({
    required this.job,
    required this.date,
    required this.enterprise,
    required this.type,
    required this.location,
    required this.task,
    required this.stack,
  });
}
